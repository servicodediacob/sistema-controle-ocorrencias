import fs from 'fs';
import path from 'path';
// Caminhos relativos a partir de 'src/scripts/'
import db from '../db'; 
import logger from '../config/logger';

// O script compilado estar√° em 'dist/scripts/'. O schema estar√° em 'dist/db/'.
// O caminho relativo de um para o outro √© '../db/schema.sql'.
// Usar path.join com __dirname torna o caminho absoluto e √† prova de erros.
const SCHEMA_FILE_PATH = path.join(__dirname, '../db/schema.sql');

async function migrate() {
  logger.info('üöÄ Iniciando a migra√ß√£o do schema do banco de dados...');
  logger.info(`üîç Procurando schema em: ${SCHEMA_FILE_PATH}`);

  const client = await db.pool.connect();
  logger.info('‚úÖ Conectado ao banco de dados.');

  try {
    logger.info('üìÑ Lendo o arquivo de schema (schema.sql)...');
    const schemaSql = fs.readFileSync(SCHEMA_FILE_PATH, 'utf-8');

    logger.info('üîÑ Executando o script para criar/atualizar as tabelas...');
    await client.query(schemaSql);
    
    logger.info('üéâ Schema do banco de dados aplicado com sucesso!');

  } catch (error) {
    logger.error({ err: error }, '‚ùå Erro durante a migra√ß√£o do schema:');
    throw error;
  } finally {
    client.release();
    logger.info('üîå Conex√£o com o banco de dados liberada.');
  }
}

migrate()
  .then(() => {
    logger.info('Migra√ß√£o conclu√≠da. O processo ser√° encerrado.');
    process.exit(0);
  })
  .catch((err) => {
    logger.error("A migra√ß√£o falhou. O processo de build ser√° encerrado.", err);
    process.exit(1);
  });
