import fs from 'fs';
import path from 'path';
// A importa√ß√£o com alias funciona aqui porque o 'tsc-alias' a corrige durante o build.
import db from '@/db'; 

// O script compilado estar√° em 'dist/scripts/'. O schema estar√° em 'dist/db/'.
// O caminho relativo de um para o outro √© '../db/schema.sql'.
const SCHEMA_FILE_PATH = path.join(__dirname, '../db/schema.sql');

async function migrate() {
  console.log('üöÄ Iniciando a migra√ß√£o do schema do banco de dados...');
  console.log(`üîç Procurando schema em: ${SCHEMA_FILE_PATH}`);

  const client = await db.pool.connect();
  console.log('‚úÖ Conectado ao banco de dados.');

  try {
    console.log('üìÑ Lendo o arquivo de schema (schema.sql)...');
    const schemaSql = fs.readFileSync(SCHEMA_FILE_PATH, 'utf-8');

    console.log('üîÑ Executando o script para criar/atualizar as tabelas...');
    await client.query(schemaSql);
    
    console.log('üéâ Schema do banco de dados aplicado com sucesso!');

  } catch (error) {
    console.error('‚ùå Erro durante a migra√ß√£o do schema:', error);
    throw error;
  } finally {
    client.release();
    console.log('üîå Conex√£o com o banco de dados liberada.');
    await db.pool.end();
    console.log('üõë Pool de conex√µes do processo de migra√ß√£o encerrado.');
  }
}

migrate().catch((err) => {
  console.error("A migra√ß√£o falhou. O processo de build ser√° encerrado.", err);
  process.exit(1);
});
