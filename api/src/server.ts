import './config/envLoader'; // Garante que as variÃ¡veis de ambiente sejam carregadas primeiro
import express from 'express';
import cors from 'cors';
import { createServer } from 'http';
import { Server as SocketIOServer } from 'socket.io';
import logger from './config/logger';
import { onSocketConnection } from './services/socketService';

// ImportaÃ§Ã£o das rotas
import authRoutes from './routes/authRoutes';
import acessoRoutes from './routes/acessoRoutes';
import plantaoRoutes from './routes/plantaoRoutes';
import ocorrenciaDetalhadaRoutes from './routes/ocorrenciaDetalhadaRoutes';
import dadosRoutes from './routes/dadosRoutes';
import { runDiagnostics } from './controllers/diagController';

// --- ConfiguraÃ§Ã£o de CORS ---
const allowedOrigins = [
  'http://localhost:5173',
  'https://sistema-ocorrencias-frontend-alpha.vercel.app'
];
const corsOptions: cors.CorsOptions = {
  origin: (origin, callback ) => {
    // Permite requisiÃ§Ãµes sem 'origin' (ex: Postman, apps mobile) ou das origens listadas
    if (!origin || allowedOrigins.includes(origin)) {
      callback(null, true);
    } else {
      logger.warn({ origin }, 'Origem bloqueada pelo CORS');
      callback(new Error('NÃ£o permitido pelo CORS'));
    }
  },
  credentials: true,
};

// --- InicializaÃ§Ã£o da AplicaÃ§Ã£o ---
const app = express();
app.use(cors(corsOptions));
app.use(express.json());

// --- Rotas PÃºblicas ---
// Rotas que nÃ£o exigem autenticaÃ§Ã£o devem vir primeiro.
app.get('/api/diag', runDiagnostics); // Rota de diagnÃ³stico (Health Check)
app.use('/api/auth', authRoutes); // Rotas de login
app.use('/api/acesso', acessoRoutes); // Rota pÃºblica para solicitar acesso

// --- Rotas Protegidas ---
// A partir daqui, todas as rotas podem (e devem) ser protegidas pelo middleware de autenticaÃ§Ã£o.
app.use('/api/plantao', plantaoRoutes);
app.use('/api/ocorrencias-detalhadas', ocorrenciaDetalhadaRoutes);
app.use('/api', dadosRoutes); // Agrupa a maioria das rotas de dados

// --- ConfiguraÃ§Ã£o do Servidor HTTP e Socket.IO ---
const httpServer = createServer(app );
const io = new SocketIOServer(httpServer, { cors: corsOptions } );
onSocketConnection(io);

// --- InicializaÃ§Ã£o do Servidor ---
const PORT = process.env.PORT || 3001;
const server = httpServer.listen(PORT, ( ) => {
  logger.info(`ðŸš€ Servidor rodando na porta ${PORT} em modo ${process.env.NODE_ENV || 'development'}`);
});

export default server;
