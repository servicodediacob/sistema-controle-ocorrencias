import express, { Request, Response } from 'express';
import cors from 'cors';
import { createServer } from 'http';
import { Server as SocketIOServer } from 'socket.io';
import './config/envLoader'; // Garante que as variÃ¡veis de ambiente sejam carregadas primeiro
import logger from './config/logger';
import { onSocketConnection } from './services/socketService';

// --- 1. IMPORTAÃ‡ÃƒO DE TODAS AS ROTAS ---
import authRoutes from './routes/authRoutes';
import acessoRoutes from './routes/acessoRoutes';
import plantaoRoutes from './routes/plantaoRoutes';
import ocorrenciasDetalhadasRoutes from './routes/ocorrenciaDetalhadaRoutes';
import dadosRoutes from './routes/dadosRoutes';
import unidadesRoutes from './routes/unidadesRoutes';
import usuariosRoutes from './routes/usuarioRoutes';
import dashboardRoutes from './routes/dashboardRoutes';
import diagRoutes from './routes/diagRoutes'; // Rota de diagnÃ³stico

// --- ConfiguraÃ§Ã£o de CORS ---
const allowedOrigins = [
  'http://localhost:5173',
  'https://siscob-iota.vercel.app'
];

const corsOptions: cors.CorsOptions = {
  origin: (origin, callback ) => {
    if (!origin || allowedOrigins.includes(origin)) {
      callback(null, true);
    } else {
      callback(new Error('Not allowed by CORS'));
    }
  },
  credentials: true,
};

const app = express();

app.use(cors(corsOptions));
app.use(express.json());

// --- 2. REGISTRO DAS ROTAS NA APLICAÃ‡ÃƒO ---
// Cada conjunto de rotas Ã© prefixado com seu caminho base.
app.use('/api/auth', authRoutes);
app.use('/api/acesso', acessoRoutes);
app.use('/api/plantao', plantaoRoutes);
app.use('/api/ocorrencias-detalhadas', ocorrenciasDetalhadasRoutes);
app.use('/api/unidades', unidadesRoutes); // Inclui /crbms
app.use('/api/usuarios', usuariosRoutes);
app.use('/api/dashboard', dashboardRoutes);
app.use('/api/diag', diagRoutes); // Rota de diagnÃ³stico agora registrada
app.use('/api', dadosRoutes); // Rotas mais genÃ©ricas (como /ocorrencias, /naturezas)

// Rota raiz da API para um simples "OlÃ¡"
app.get('/api', (_req: Request, res: Response) => {
  res.status(200).json({ message: 'API do Sistema de OcorrÃªncias do COCB no ar!' });
});

// --- ConfiguraÃ§Ã£o do Servidor HTTP e Socket.IO ---
const httpServer = createServer(app );
const io = new SocketIOServer(httpServer, {
  cors: corsOptions
} );

// Inicializa o serviÃ§o de socket
onSocketConnection(io);

const PORT = process.env.PORT || 3001;

const server = httpServer.listen(PORT, ( ) => {
  logger.info(`ðŸš€ Servidor rodando na porta ${PORT}`);
});

export default server;
